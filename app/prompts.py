PLANNER_PROMPT = """你是一个 agent 流程规划器。看用户的问题，规划 2~6 步思考流程。

每一步必须包含：
- title: 一句话短标题（≤10 字），展示给用户看
- kind: 步骤类型，从下面挑：
    analysis    - 拆解问题、理解用户意图与背景
    retrieval   - 查询公司内部 IT 知识库（VPN/邮箱/账号/打印机/网络等内部资源），instruction 即检索 query
    reasoning   - 基于已知信息推断、对比、判断
    summarize   - 综合前面几步整理结论
    answer      - 直接产出最终回答给用户（必须是流程的最后一步）
- instruction: 对执行者的具体指令（≤80 字）

约束：
- 流程最后一步必须是 kind=answer
- 简单问题（如「快排怎么写」）→ 2~3 步即可，不要乱加 retrieval
- 涉及公司内部 IT 资源 → 至少加一步 retrieval
- 复杂分析问题最多拆 6 步

严格只输出 JSON，不要任何额外文字：
{"steps":[{"title":"...","kind":"...","instruction":"..."}, ...]}"""


THOUGHT_STEP_PROMPT = """你正在执行一个思考流程的第 {idx}/{total} 步。

【用户原问题】
{question}

【已完成步骤的产出】
{prior}

【当前步骤】
- 标题: {title}
- 类型: {kind}
- 指令: {instruction}

围绕这一步的指令完成它。同时给出 2 条不同角度的备选思路，方便用户切换重跑。

要求：
- primary 是这一步的主要分析/推断/总结，控制在 200 字以内
- alternatives 是另外 2 个不同的角度（不能与 primary 雷同），每条同样 ≤200 字
- alt_summaries 是每条备选思路的一句话特征描述（≤12 字），突出这条思路的侧重点或与 primary 的区别

严格只输出 JSON：
{{"primary":"...", "alternatives":["...", "..."], "alt_summaries":["特征一(≤12字)", "特征二(≤12字)"]}}"""


