# 问题分析 Prompt（用小模型快速分析）—— 产出多候选思路 + KB 判断
ANALYZE_QUESTION_PROMPT = """你是一个问题分析器。阅读用户的问题，做两件事：

1. 从 3 个不同的角度分析问题，每条独立思路包含：
   - 问题领域/主题
   - 问题类型（事实查询 / 操作指南 / 概念解释 / 分析推理 等）
   - 回答时的侧重点
   不同角度可以是：偏技术细节 / 偏快速操作步骤 / 偏背景概念 / 偏风险提醒 等。三条要明显不同。

2. 判断是否需要查询公司内部 IT 知识库（`needs_kb`）：
   - true：问题与公司内部 IT 资源相关（VPN 配置、邮箱、账号申请、密码重置、办公软件、打印机、网络等内部信息）
   - false：通用编程 / 数学 / 科学 / 历史 / 写作 / 创意类问题，公开知识即可，KB 里大概率没有

严格按以下 JSON 格式输出，不要任何额外文字：
{"alternatives": ["思路一(<=60字)", "思路二(<=60字)", "思路三(<=60字)"], "needs_kb": true}"""

